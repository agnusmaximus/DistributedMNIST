def extract_data(fraw):
    return [[float(y) for y in x.split(" ")[1:]] for x in fraw.splitlines() if x != ""]

def extract_all_data(raw_data):
    data_all = {}
    for k,v in raw_data.items():
        data_all[k] = extract_data(v)
    return data_all

def extract_field(data, field):
    field_map = {
        "time" : 0,
        "epoch" : 1,
        "accuracy" : 2,
        "loss" : 3
    }

    return [x[field_map[field]] for x in data]

